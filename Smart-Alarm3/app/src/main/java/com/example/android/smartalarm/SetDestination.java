    class GooglePlacesAutocompleteAdapter extends ArrayAdapter<String> implements Filterable {
        private ArrayList<String> resultList;

        public GooglePlacesAutocompleteAdapter(Context context, int textViewResourceId) {
            super(context, textViewResourceId);
        }

        @Override
        public int getCount() {
            return resultList.size();
        }

        @Override
        public String getItem(int index) {
            return resultList.get(index);
        }

        @Override
        public Filter getFilter() {
            Filter filter = new Filter() {
                @Override
                protected FilterResults performFiltering(CharSequence constraint) {
                    FilterResults filterResults = new FilterResults();
                    if (constraint != null) {
                        // Retrieve the autocomplete results.
                        resultList = autocomplete(constraint.toString());

                        // Assign the data to the FilterResults
                        filterResults.values = resultList;
                        filterResults.count = resultList.size();
                    }
                    return filterResults;
                }

                @Override
                protected void publishResults(CharSequence constraint, FilterResults results) {
                    if (results != null && results.count > 0) {
                        notifyDataSetChanged();
                    } else {
                        notifyDataSetInvalidated();
                    }
                }
            };
            return filter;
        }
    }

    private class RetrieveLatitudeLongitude extends AsyncTask<String, String, String> {

        private Exception exception;
        Context context;
        public RetrieveLatitudeLongitude(Context c){
            this.context =c;
        }

        protected String doInBackground(String... youraddress) {
            String addr = youraddress[0];
            try {
                String uri = "http://maps.google.com/maps/api/geocode/json?address=" +
                        addr + "&sensor=false";
                HttpGet httpGet = new HttpGet(uri);
                HttpClient client = new DefaultHttpClient();
                HttpResponse response;
                StringBuilder stringBuilder = new StringBuilder();
                LocationManager lm = (LocationManager)getSystemService(Context.LOCATION_SERVICE);
                Location location = lm.getLastKnownLocation(LocationManager.GPS_PROVIDER);
                double longitude = location.getLongitude();
                double latitude = location.getLatitude();
                final String src_lon = longitude + "";
                final String src_lat = latitude + "";
                int alarmid = dbHelper.maxAlarmId();
                for (AlarmModel a : dbHelper.getAlarms()) {
                    Log.d(LOG_TAG, a.id + ": " + a.getInSec());
                }

                String alarm_time = dbHelper.getAlarm(alarmid).getInSec() + "";
                String salarm_id = dbHelper.getAlarm(alarmid).id + "";
                dbHelper.close();
                try {
                    response = client.execute(httpGet);
                    HttpEntity entity = response.getEntity();
                    InputStream stream = entity.getContent();
                    int b;
                    while ((b = stream.read()) != -1) {
                        stringBuilder.append((char) b);
                    }
                } catch (ClientProtocolException e) {
                    e.printStackTrace();
                } catch (IOException e) {
                    e.printStackTrace();
                }

                JSONObject jsonObject;
                try {
                    jsonObject = new JSONObject(stringBuilder.toString());

                    double lng = ((JSONArray)jsonObject.get("results")).getJSONObject(0)
                            .getJSONObject("geometry").getJSONObject("location")
                            .getDouble("lng");

                    double lat = ((JSONArray)jsonObject.get("results")).getJSONObject(0)
                            .getJSONObject("geometry").getJSONObject("location")
                            .getDouble("lat");

                    Bundle data = new Bundle();
                    data.putString("dest_lon", Double.toString(lng));
                    data.putString("dest_lat", Double.toString(lat));
                    data.putString("src_lon", src_lon);
                    data.putString("src_lat", src_lat);
                    data.putString("alarm_time", alarm_time);
                    data.putString("salarm_id", salarm_id);
                    String id = Integer.toString(msgId.incrementAndGet());

                    Log.d(LOG_TAG, "Sending: " + data.toString());

                    gcm.send(SENDER_ID + "@gcm.googleapis.com", id, data);

                    return lat + " " + lng;
                } catch (JSONException e) {
                    e.printStackTrace();
                } catch (IOException ix){
                    ix.printStackTrace();
                }
                return null;
            } catch (Exception e) {
                this.exception = e;
                e.printStackTrace();
                return null;
            }
        }

        protected void onPostExecute(String result) {
            String[] latlng = result.split(" ");
            LatLng destination = new LatLng(Double.parseDouble(latlng[0]), Double.parseDouble(latlng[1]));

            if (map!=null){
                map.addMarker(new MarkerOptions().position(destination).title("current"));
            }

            map.animateCamera(CameraUpdateFactory.newLatLngZoom(destination, 15.5f), 4000, null);
            Toast.makeText(context, "Destination Set!", Toast.LENGTH_LONG).show();
        }
    }

